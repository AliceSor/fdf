#include "fdf.h"

void			put_pixel(double x, double y, t_fdf *fdf, int color)
{
	int			i;

	i = 1;
	if (i && x >= 0 && x < 1000 && y >= 0 && y < 1000)
	{
		i = (int)(fdf->ls * (y) / 4 + x);
		if (i < 1000000 - 2 && i > 0)
			fdf->imdat[i] = color;
	}
}
