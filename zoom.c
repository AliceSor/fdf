#include "fdf.h"

void	zoom(t_point *point, t_fdf *fdf)
{
	if (fdf && point)
	{
		X -= fdf->max_x / 2;
		Y -= fdf->max_y / 2;
		Z -= fdf->max_z / 2;
	}
}
