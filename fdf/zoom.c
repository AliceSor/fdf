#include "fdf.h"

void zoom(t_point *point, t_fdf *fdf)
{
	if (fdf && point)
	{
		X *= 20;
		Y *= 20;
		Z *= -10;

		point->x_r = X;
		point->y_r = Y;
		point->z_r = Z;
	}
}
