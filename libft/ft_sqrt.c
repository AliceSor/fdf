int	ft_sqrt(int nb)
{
	int		i;

	i = 2;
	if (nb < 1)
	{
		return (0);
	}
	else
	{
		while (i < nb)
		{
			if (nb == i * i)
			{
				return (i);
			}
			i++;
		}
		return (0);
	}
}
